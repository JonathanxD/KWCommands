package com.github.jonathanxd.kwcommands.reflect.annotation

/**
 * Marks an function as the handler of the class command.
 *
 * Obs: If the declaring class of annotated function is not a command class (in other words, is not annotated with [Cmd]),
 * this annotation will have no effect.
 *
 * ObsÂ²: Does not annotate more than one function with this annotation in the same class, KWCommands will always
 * consider first found function, which is unpredictable.
 */
@Retention(AnnotationRetention.RUNTIME)
@Target(AnnotationTarget.FUNCTION)
annotation class CmdHandler